num1 = 10
num2 = 2

#Addition
result1 = num1 + num2
print("Addition", result1)

#subtraction
result2 = num1 - num2
print("Subtraction", result2)

#Multiplication
result3 = num1 * num2
print("Multiplication", result3)

#division
result4 = num1 // num2
print("Division", result4)

#modulus (Remainder)
result5 = num1 % num2
print("Remainder  ", result5)
